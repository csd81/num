function z=f(u)     %the function to be minimized, u is a two dimensional vector
    z=(u(1)^2-2*u(2))^2+2*(u(1)-1)^2;
end

function z=df(u)     %the gradient vector of the function f(u), z is a row vector
    z=[4*(u(1)^2-2*u(2))*u(1)+4*(u(1)-1);-4*(u(1)^2-2*u(2))];
end

function z=ddf(u)     %the Hesse matrix of the function f(u), z is a 2x2 matrix
    z=[12*u(1)^2-8*u(2)+4,-8*u(1);
      -8*u(1),8];
end

function z=fv(x,y)      %evaluate the function f at the points (x(i,j),y(i,j)), 
                        %where x and y are input matrices generated by meshgrid
                        %the output z is the matrix of the function values
    [n,m]=size(x);
    z=zeros(n,m);
    for i=1:n
        for j=1:m
            z(i,j)=f([x(i,j),y(i,j)]);
        end
    end
end
